#!/bin/bash

# Получава при стартиране като параметър в командния ред идентификатор на потребител.
# Скриптът периодично (sleep(1)) да проверява дали потребителят е log-нат,
# и ако да - да прекратява изпълнението си, извеждайки на стандартния изход подходящо съобщение.

read -p "Username: " user
while [[ true ]]
do
	if [[ $(who | grep $user) ]]; then
		echo "logged in"
		break;
	fi
	echo "checking..."
	sleep 1
done